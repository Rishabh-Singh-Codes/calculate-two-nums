export const addTwoNumbers = (num1: number, num2: number) => {
  return num1 + num2;
};

export const subtractTwoNumbers = (num1: number, num2: number) => {
  return num1 - num2;
};

export const multiplyTwoNumbers = (num1: number, num2: number) => {
  return num1 * num2;
};

export const divideTwoNumbers = (num1: number, num2: number) => {
  return num1 / num2;
};

export const modTwoNumbers = (num1: number, num2: number) => {
  return num1 % num2;
};
